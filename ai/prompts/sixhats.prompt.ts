// ملف: ai/prompts/sixhats.prompt.ts
// الوصف: تقنية قبعات التفكير الست (Six Hats)

import { PersonaConfig } from "../types";

export function buildSixhatsPrompt(
    persona: PersonaConfig,
    topic: string,
    context?: string
): string {
    return `أنت ${persona.name}، ${persona.personality}.
الموضوع: ${topic}
${context ? `السياق: ${context}` : ""}

التعليمات:
- استخدم طريقة "قبعات التفكير الست" لأخذ زوايا مختلفة.
- فكر كأنك تضع قبعات متعددة: القبعة البيضاء (حقائق)، الحمراء (مشاعر وانطباعات)، السوداء (مخاطر وتحذيرات)، الصفراء (إيجابيات وفوائد)، الخضراء (بدائل وابتكار)، والزرقاء (تنظيم ونظرة شمولية).
- قدم 6 أفكار بناءً على كل قبعة من القبعات (بواقع فكرة لكل واحدة). تأكد من التحدث الدائم بأسلوبك الشخصي المميز: ${persona.writingStyle}

الإخراج: إرجاع كائن JSON حصرياً:
{
  "ideas": [
    {
      "content": "طرح يمثل القبعة المحددة واسمها",
      "reasoning": "المنطق أو الدافع من زاوية القبعة المستخدمة مع مسحة شخصيتك"
    }
  ]
}

مثال:
{
  "ideas": [
    {
      "content": "القبعة البيضاء: دراسة البيانات تظهر أننا نفقد 40% عند صفحة الدفع",
      "reasoning": "من الجيد الاعتماد على الإحصائيات التي لا غبار عليها كمؤشر للانطلاق."
    }
  ]
}`;
}
