// ملف: ai/prompts/reverse.prompt.ts
// الوصف: تقنية العصف الذهني العكسي (Reverse Brainstorming)

import { PersonaConfig } from "../types";

export function buildReversePrompt(
    persona: PersonaConfig,
    topic: string,
    context?: string
): string {
    return `أنت ${persona.name}، ${persona.personality}.
الموضوع: ${topic}
${context ? `السياق: ${context}` : ""}

التعليمات:
- استخدم تقنية "العصف الذهني العكسي". اقلب المشكلة: كيف نفشل؟ كيف نجعل الوضع أسوأ؟ ثم اعكس هذا "الفشل" لاستخراج حل حقيقي مفيد.
- يجب أن تقدم 5 أفكار تستند إلى هذه المفارقة.
- أسلوب الإجابة يجب أن يتطابق مع طابعك: ${persona.writingStyle}

الإخراج: إرجاع كائن JSON حصرياً:
{
  "ideas": [
    {
      "content": "الحل الإيجابي المستنبط من الفشل",
      "reasoning": "كيف قمنا بعكس المشكلة للحصول على هذا الحل (مثال: لنجعل التطبيق أسوأ يمكننا إخفاء كل الأزرار، إذاً الحل هو توضيح الأزرار المهمة وتمييزها)."
    }
  ]
}

مثال:
{
  "ideas": [
    {
      "content": "تصميم واجهة مستخدم بألوان تباين عالية وخطوط واضحة",
      "reasoning": "عكسياً، أسوأ شيء هو وضع نصوص باهتة على خلفية بيضاء تجعل القراءة مستحيلة، لذا يجب أن يكون التباين في أعلى مستوياته لتسهيل الرؤية."
    }
  ]
}`;
}
