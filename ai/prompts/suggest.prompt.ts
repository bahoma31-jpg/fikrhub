// ملف: ai/prompts/suggest.prompt.ts
// الوصف: اقتراح خطوات مستقبلية وتوصيات بناءً على مُخرجات الجلسة الكاملة

import { PersonaConfig } from "../types";

export function buildSuggestPrompt(
    persona: PersonaConfig,
    topic: string, // يمكن أن يكون عبارة عن ملخص للجلسة أو الأفكار
    context?: string
): string {
    return `أنت ${persona.name}، ${persona.personality}.
مخرجات الجلسة الذهنية: ${topic}
${context ? `السياق: ${context}` : ""}

التعليمات:
- اقترح 3-5 توصيات استراتيجية أو خطوات قادمة لضمان تنفيذ المخرجات أو تحسين العملية بناءً على نتيجة الجلسة ككل.
- راعِ أسلوب كتابتك وطريقة تفكيرك: ${persona.writingStyle}

الإخراج: إرجاع كائن JSON حصرياً يحتوي على قائمة التوصيات كأفكار:
{
  "ideas": [
    {
      "content": "الخطوة العملية المقترحة",
      "reasoning": "سبب منطقي لوجوب المباشرة بهذه الخطوة فوراً"
    }
  ]
}

مثال:
{
  "ideas": [
    {
      "content": "تشكيل فريق مصغر للبدء بدراسة أول فكرتين فقط",
      "reasoning": "التشتيت هو عدو النجاح؛ التركيز على الأولوية سيولد نتائج قابلة للقياس السريع، وهذا أمر منطقي تماماً."
    }
  ]
}`;
}
